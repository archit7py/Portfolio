// Dynamic cursor-following glow effect for stat elements
document.querySelectorAll('.stat').forEach(stat => {
    stat.addEventListener('mousemove', (e) => {
        const rect = stat.getBoundingClientRect();
        const x = ((e.clientX - rect.left) / rect.width) * 100;
        const y = ((e.clientY - rect.top) / rect.height) * 100;
        
        stat.style.setProperty('--mouse-x', `${x}%`);
        stat.style.setProperty('--mouse-y', `${y}%`);
    });
    
    stat.addEventListener('mouseleave', () => {
        stat.style.setProperty('--mouse-x', '50%');
        stat.style.setProperty('--mouse-y', '50%');
    });
}); 

